class Gallery extends Component {
  state = {
    activeThumbnail: imagesList[0].id,
  }

  clickThumbnailItem = id => {
    this.setState({activeThumbnail: id})
  }

  render() {
    const {activeThumbnail} = this.state
    const {imageUrl, imageAltText} = imagesList[activeThumbnail]

    return (
      <div className="bg-container">
        <img className="big-image" src={imageUrl} alt={imageAltText} />
        <h1 className="desc">Nature Photography</h1>
        <p className="thumb-desc">Nature Photography by Rahul</p>
        <ul className="thumbnail-container">
          {imagesList.map(eachThumbnail => (
            <ThumbnailItem
              thumbnailList={eachThumbnail}
              key={eachThumbnail.id}
              clickThumbnailItem={this.clickThumbnailItem}
              isActive={activeThumbnail === eachThumbnail.id}
            />
          ))}
        </ul>
      </div>
    )
  }
}

